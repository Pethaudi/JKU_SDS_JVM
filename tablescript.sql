CREATE TABLE POKEMON(
  id INTEGER PRIMARY KEY,
  name VARCHAR2(30),
  height FLOAT,
  weight FLOAT,
  type VARCHAR2(30),
  category VARCHAR2(30),
  hp INTEGER,
  attack INTEGER,
  defense INTEGER,
  sum INTEGER,
  maxTournamentpoints INTEGER
);

grant all on POKEMON to in140068;

